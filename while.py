#while loop
i = 0
while i < 11:
  print(i)
  i += 1



#Break statement
i = 1
while i < 6:
  print(i)
  if (i == 3):
    break
  i += 1

#continue statement
i = 1
while i < 6:
  print(i)
  if (i == 3):
    break
  i += 1


#else statement
i = 1
while i < 11:
  print(i)
  i += 1
else:
  print("i is no longer less than 11")