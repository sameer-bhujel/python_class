#creating variables
x = 5
y = "Sameer"
print(x)
print(y)

#Casting
x = str(5)
y = int(5)
z = float(5)
print(x)
print(y)
print(z)

#Get the type
x = 5
y = "Sameer"
print(type(x))
print(type(y))

# Double quotes or single quotes
x = "Sameer"
print(x)

x = 'Sameer'
print(x)

#Many values to multiple variables
x, y, z = "It", "Bridge", "Banepa"

print(x)
print(y)
print(z)

#One value to multiple variables
x = y = z = "Banepa"

print(x)
print(y)
print(z)

#Unpack a collection
Citys = ["Banepa", "Bhaktapur", "Kathmandu"]
x, y, z = Citys

print(x)
print(y)
print(z)


